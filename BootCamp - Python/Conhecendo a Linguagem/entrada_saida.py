# Funções de Entrada e Saída
# Ex: 
nome = input("Escreva seu nome: ") # recebe nome
sobrenome = input("Escreva seu sobrenome: ") # recebe sobrenome

print("Meu nome completo é:", nome, sobrenome) # imprime o nome e o sobrenome na mesma linha
print("Meu nome completo é:", nome, sobrenome, end='...\n') # imprime o mesmo adicionando ... e uma quebra de linha \n
print("Meu nome completo é:", nome, sobrenome, sep='_') # imprimero o mesmo e transforma a separação no que estiver entre ' '