# Exemplo de variáveis 

idade = 30
nome = 'Marcos'
print(f'Meu nome é {nome} e eu tenho {idade} anos(s) de idade')

# Exemplo de varável na mesma linha
idade, nome = (30, 'Rayane') # O abrir e fechar () é opcional. Os valores devem ser separados por vírgula e em ordem.
print(f'Meu nome é {nome} e eu tenho {idade} anos(s) de idade')

