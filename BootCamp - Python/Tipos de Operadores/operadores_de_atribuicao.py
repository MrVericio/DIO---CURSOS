# Atribuição simples
# Ex
saldo = 500
print("Atribuição simples:", saldo)

# Atirbuição com adição
saldo = 500
saldo += 200
print("Atribuição com adição:", saldo)

# Atirbuição com subtração
saldo = 500
saldo -= 200
print("Atribuição com subtração:", saldo)

# Atirbuição com multiplicação
saldo = 500
saldo *= 200
print("Atribuição com multiplicação:", saldo)

# Atirbuição com divisão
saldo = 500
saldo /= 200
print("Atribuição com divisão:", saldo)

# Atirbuição com divisão inteira
saldo = 500
saldo //= 200
print("Atribuição com divisão inteira:", saldo)

# Atirbuição com módulo
saldo = 500
saldo %= 200
print("Atribuição com módulo:", saldo)

# Atirbuição com exponenciação
saldo = 500
saldo **= 2
print("Atribuição com exponenciação:", saldo)